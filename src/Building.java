public class Building {
  private double xCoord;
  private double yCoord;

  public double getxCoord() {
      return xCoord;
  }

  public double getyCoord() {
      return yCoord;
  }

  public void setyCoord(double yCoord) {
      this.yCoord = yCoord;
  }

  public void setxCoord(double xCoord) {
      this.xCoord = xCoord;
  }
}
